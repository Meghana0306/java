public class LogicalOperator {
    public static void main(String args[]){
        int a = 5;
        int b= 4;
        System.out.println("And operator "+((a<b)&&(b<a)));
        System.out.println("OR operator "+((a<b)||(b<a)));
        System.out.println("Not operator "+(!(a>b)));
        


    }
}
